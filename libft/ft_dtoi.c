#include "libft.h"

int	ft_dtoi(double nb)
{
	int	i;

	i = 0;
	if (nb < 0)
	{
		while (i > nb)
			i--;
		i++;
	}
	else
	{
		while (i < nb)
			i++;
		i--;
	}
	return (i);
}
